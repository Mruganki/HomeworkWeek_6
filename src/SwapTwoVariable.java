public class SwapTwoVariable {
    //swap to variable
        public static void main(String[] args){
            String x,y, omg;// string varables
            x= "Herry";//define variable
            y= "Porter";//define variable
            System.out.println("before swapping: x,y = "+x+", "+y);//printstatement
            omg = x;// variable in replacement condition
            x= y;// variable replacement condition
            y= omg;// variable replacement condition
            System.out.println("after swapping : x,y= "+x+", "+y);// printstatement
        }
}




